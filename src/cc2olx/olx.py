import io
import tarfile
import xml.dom.minidom

class OlxExport:
    def __init__(self, cartridge):
        self.cartridge = cartridge
        self.doc = None

    def xml(self):
        impl = xml.dom.minidom.getDOMImplementation()
        self.doc = impl.createDocument(None, "course", None)
        xcourse = self.doc.documentElement
        xcourse.setAttribute("org", "hack-xxiv")
        xcourse.setAttribute("course", "Some_cc_Course")
        xcourse.setAttribute("name", "Some_cc_Course")
        xcourse.appendChild(self.doc.createComment(" Generated by cc2olx "))
        tags = "chapter sequential vertical".split()
        self._add_olx_nodes(xcourse, self.cartridge.normalized['children'], tags)
        return self.doc.toprettyxml()

    def _add_olx_nodes(self, elt, data, tags):
        leaf = False
        if not tags:
            tags = ["html"]
            leaf = True
        for dd in data:
            child = self.doc.createElement(tags[0])
            if "title" in dd:
                child.setAttribute("display_name", dd["title"])
            if leaf:
                content = None
                if "identifierref" in dd:
                    idref = dd["identifierref"]
                    content = self.cartridge.get_resource_content(idref)
                if content is None:
                    content = "<p>MISSING CONTENT</p>"
                txt = self.doc.createTextNode(content)
                child.appendChild(txt)
            elt.appendChild(child)
            if "children" in dd:
                self._add_olx_nodes(child, dd["children"], tags[1:])

